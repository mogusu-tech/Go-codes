#Arithmetic Operators
num1 = 34
num2 = 10
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)#Modulus - Returns a remainder

#Comparison Operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)#Equal to
print(num1 != num2)

#Assignment Operators
w = 7
print(w)
w+= 3
print(w)
b = 10
#b -= 2
print(b)
b *= 2
print(b)

#logic Operators -and,or,not
print(56<100 and 45<23)
print(56<100 or 45<23)
print(not(56<100 or 45<23))