#While loop

#Increasing values
number = 20
while number <=25:
    print(number)
    number += 1


#Decreasing values
number = 100
while number >1:
    print(number)
    number -= 1
#For loop
for x in range(1,5):
    print(x)

for num in range(20,31,2):
    print(num)

#List
languages = ["Python","Kotlin","PHP"]
for language in languages:
    print(language)

